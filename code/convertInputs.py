def convert(key):
    if key == "p":
        return 1
    if key == "q":
        return 0x10
    if key == "w":
        return 0x11
    if key == "e":
        return 0x12
    if key == "r":
        return 0x13
    if key == "t":
        return 0x14
    if key == "m":
        return 0x32
    if key == "n":
        return 0x31
    if key == "b":
        return 0x30
    if key == "v":
        return 0x2F
    if key == "c":
        return 0x2E
    if key == "x":
        return 0x2D
    if key == "z":
        return 0x2C
    if key == "j":
        return 0x24
    if key == "h":
        return 0x23
    if key == "g":
        return 0x22
    if key == "f":
        return 0x21
    if key == "d":
        return 0x20
    if key == "s":
        return 0x1F
    if key == "a":
        return 0x1E
    if key == "u":
        return 0x16
    if key == "y":
        return 0x15
    return 0